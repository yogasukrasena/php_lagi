//Insert Method @2-0565BE9B
    function Insert()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->CmdExecution = true;
        $this->cp["ClientName"] = new clsSQLParameter("ctrlClientName", ccsText, "", "", $this->ClientName->GetValue(true), "", false, $this->ErrorBlock);
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildInsert", $this->Parent);
        if (!is_null($this->cp["ClientName"]->GetValue()) and !strlen($this->cp["ClientName"]->GetText()) and !is_bool($this->cp["ClientName"]->GetValue())) 
            $this->cp["ClientName"]->SetValue($this->ClientName->GetValue(true));
        $this->InsertFields["ClientName"]["Value"] = $this->cp["ClientName"]->GetDBValue(true);
        $this->SQL = CCBuildInsert("tbladminist_administ_doc", $this->InsertFields, $this);
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeExecuteInsert", $this->Parent);
        if($this->Errors->Count() == 0 && $this->CmdExecution) {
            $this->query($this->SQL);
            $this->CCSEventResult = CCGetEvent($this->CCSEvents, "AfterExecuteInsert", $this->Parent);
        }
    }
