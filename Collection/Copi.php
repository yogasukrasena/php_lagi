    }
//End SetValues Method

//Update Method @5-B2CB3655
    function Update()
    {
        global $CCSLocales;
        global $DefaultDateFormat;
        $this->CmdExecution = true;
        $this->cp["RefNote"] = new clsSQLParameter("ctrlRefNote", ccsText, "", "", $this->RefNote->GetValue(true), NULL, false, $this->ErrorBlock);
        $wp = new clsSQLParameters($this->ErrorBlock);
        $wp->AddParameter("1", "ctrlRefID", ccsInteger, "", "", $this->RefID->GetValue(true), "", false);
        if(!$wp->AllParamsSet()) {
            $this->Errors->addError($CCSLocales->GetText("CCS_CustomOperationError_MissingParameters"));
        }
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeBuildUpdate", $this->Parent);
        if (!is_null($this->cp["RefNote"]->GetValue()) and !strlen($this->cp["RefNote"]->GetText()) and !is_bool($this->cp["RefNote"]->GetValue())) 
            $this->cp["RefNote"]->SetValue($this->RefNote->GetValue(true));
        $wp->Criterion[1] = $wp->Operation(opEqual, "ID", $wp->GetDBValue("1"), $this->ToSQL($wp->GetDBValue("1"), ccsInteger),false);
        $Where = 
             $wp->Criterion[1];
        $this->UpdateFields["RefNote"]["Value"] = $this->cp["RefNote"]->GetDBValue(true);
        $this->SQL = CCBuildUpdate("tblreference", $this->UpdateFields, $this);
        $this->SQL = CCBuildSQL($this->SQL, $Where, "");
        $this->CCSEventResult = CCGetEvent($this->CCSEvents, "BeforeExecuteUpdate", $this->Parent);
        if($this->Errors->Count() == 0 && $this->CmdExecution) {
            $this->query($this->SQL);
            $this->CCSEventResult = CCGetEvent($this->CCSEvents, "AfterExecuteUpdate", $this->Parent);
        }
    }
//End Update Method

