        $this->Initialize();
        $this->Sukses = new clsField("Sukses", ccsText, "");
        

        $this->UpdateFields["RefNote"] = array("Name" => "RefNote", "Value" => "", "DataType" => ccsText, "OmitIfEmpty" => 1);
    }
